public class LocationService extends Service implements LocationListener {
    LocationManager locationManager;
    final static long minTime = 1* 1000;
    final static float minDistance = 1;
    DatabaseHandler dbHadler;
    SQLiteDatabase sqLiteDatabase;

    @Override
    public void onCreate() {
        super.onCreate();
          /*-----------*/
        dbHadler = new DatabaseHandler(getApplicationContext());
        sqLiteDatabase = dbHadler.getWritableDatabase();
        dbHadler.clearLocation();
        /*-----------*/
        locationManager = (LocationManager) getApplicationContext().getSystemService(Context.LOCATION_SERVICE);
        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_FINE_LOCATION) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_COARSE_LOCATION) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            Toast.makeText(getApplicationContext(),"NOT SUPPORTED",Toast.LENGTH_SHORT).show();
            return;
        }
        locationManager.requestLocationUpdates(LocationManager.GPS_PROVIDER, minTime, minDistance, this);
    }

    @Nullable
    @Override
    public IBinder onBind(Intent intent) {
        return null;
    }

    @Override
    public void onLocationChanged(Location location) {
        // Gets the changed location
        // And Appends the location to the JSON String
            String latlng = location.getLatitude() + "," + location.getLongitude();
            Long tsLong = System.currentTimeMillis()/1000;
            String timeStamp = tsLong.toString();
            dbHadler.addLocation(location,timeStamp);
    }

    @Override
    public void onStatusChanged(String provider, int status, Bundle extras) {

    }

    @Override
    public void onProviderEnabled(String provider) {

    }

    @Override
    public void onProviderDisabled(String provider) {

    }

    @Override
    public void onDestroy()
    {
        Log.e("LOcation ", "onDestroy");
        super.onDestroy();
        if (locationManager != null) {
            locationManager.removeUpdates(this);
        }
    }
}



